class CreatePythonTransformations:

    @staticmethod
    def _prepare_generated_file(file_path_str: str, logger) -> bool:
        """
        Prepare a file for generation by checking if it has been manually edited.

        Lifecycle logic:
        - If .py exists and .generated exists: compare them
          - If different: .py has manual edits, preserve it, delete .generated (will recreate)
          - If same: safe to regenerate
        - If .py exists but no .generated: first time generating, preserve current .py

        Args:
            file_path_str: Path to the .py file to generate
            logger: Logger instance

        Returns:
            bool: True if we should preserve existing .py file, False if safe to overwrite
        """
        file_path = Path(file_path_str)
        generated_path = Path(str(file_path_str) + '.generated')

        # If .py doesn't exist, nothing to preserve
        if not file_path.exists():
            return False

        # If .generated doesn't exist, this is first generation - preserve .py
        if not generated_path.exists():
            logger.info(f"Preserving existing {file_path.name} (no .generated base found)")
            return True

        # Both exist - compare them
        try:
            with open(file_path, 'r', encoding='utf-8') as f1:
                current_content = f1.read()
            with open(generated_path, 'r', encoding='utf-8') as f2:
                generated_content = f2.read()

            if current_content != generated_content:
                logger.info(f"Preserving manually edited {file_path.name}")
                # Delete old .generated, we'll create new one
                generated_path.unlink()
                return True
            else:
                # Files are identical, safe to regenerate
                logger.info(f"No edits detected in {file_path.name}, regenerating")
                return False
        except Exception as e:
            logger.error(f"Error comparing files: {e}")
            return True  # Preserve on error

    @staticmethod
    def _finalize_generated_file(file_path_str: str, preserve_original: bool, logger):
        """
        Finalize generation by creating .generated base copy.

        Args:
            file_path_str: Path to the generated .py file
            preserve_original: If True, original .py was preserved, copy to .generated
            logger: Logger instance
        """
        file_path = Path(file_path_str)
        generated_path = Path(str(file_path_str) + '.generated')

        try:
            if preserve_original:
                # Original .py was preserved with edits, new generation was skipped
                # Just log this state
                logger.info(f"Skipped generation of {file_path.name} (manual edits preserved)")
            else:
                # New file was generated, save as .generated base
                if file_path.exists():
                    shutil.copy2(file_path, generated_path)
                    logger.info(f"Created base reference: {generated_path.name}")
        except Exception as e:
            logger.error(f"Error finalizing generated file: {e}")

    @staticmethod
    def _write_generation_metadata(file, filename: str):
        """
        Write generation metadata header to file.

        Args:
            file: Open file handle
            filename: Name of the file being generated
        """
        timestamp = datetime.now().isoformat()
        file.write(f"# Generated: {timestamp}\n")
        file.write(f"# Generator: CreatePythonTransformations (ANCRDT Lifecycle)\n")
        file.write(f"# File: {filename}\n")
        file.write(f"# DO NOT EDIT THIS FILE DIRECTLY - Edit via web UI to preserve changes\n")
        file.write(f"# Base version saved as {filename}.generated\n\n")

