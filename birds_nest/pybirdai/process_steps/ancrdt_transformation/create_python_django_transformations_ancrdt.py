    def create_output_classes(sdd_context,logger):
        """Generate output table classes for ANCRDT with lifecycle management (AST-based)"""

        file_path = sdd_context.output_directory + os.sep + 'generated_python_joins' + os.sep +  'ancrdt_output_tables.py'
        filename = 'ancrdt_output_tables.py'

        # Check if file has manual edits
        preserve = CreatePythonTransformations._prepare_generated_file(file_path, logger)
        if preserve:
            logger.info(f"Skipping generation of {filename} - manual edits detected")
            return

        print(f"DEBUG: cube_link_to_foreign_cube_map has {len(sdd_context.cube_link_to_foreign_cube_map)} entries")
        print(f"DEBUG: Keys: {list(sdd_context.cube_link_to_foreign_cube_map.keys())[:5]}")

        cube_link_to_foreign_cube_map__ = {
            rolc_id:cube_links
            for rolc_id, cube_links in sdd_context.cube_link_to_foreign_cube_map.items()
            if "ANCRDT" in rolc_id
        }
        print(f"DEBUG: Filtered to {len(cube_link_to_foreign_cube_map__)} ANCRDT entries")
        print(f"DEBUG: bird_cube_structure_item_dictionary has {len(sdd_context.bird_cube_structure_item_dictionary)} keys")
        print(f"DEBUG: Sample keys in bird_cube_structure_item_dictionary: {list(sdd_context.bird_cube_structure_item_dictionary.keys())[:10]}")

        # Collect all imports
        from pybirdai.process_steps.ancrdt_transformation.ancrdt_transformation_func.ast_builders import create_import
        imports = [
            create_import('pybirdai.process_steps.pybird.orchestration', ['Orchestration']),
            create_import('datetime', ['datetime']),
            create_import('pybirdai.annotations.decorators', ['lineage', 'track_table_init']),
        ]

        # Collect all classes
        all_classes = []

        for rolc_id, cube_links in cube_link_to_foreign_cube_map__.items():
            print(f"DEBUG: Processing rolc_id={rolc_id}, looking for key={rolc_id}")

            # Add relative import for logic module
            imports.append(create_import(f'.{rolc_id}_logic', None))  # from .{rolc_id}_logic import *

            if rolc_id in sdd_context.bird_cube_structure_item_dictionary:
                cube_structure_items = sdd_context.bird_cube_structure_item_dictionary[rolc_id]
                print(f"DEBUG: Found {len(cube_structure_items)} cube_structure_items for rolc_id={rolc_id}")
            else:
                print(f"ERROR: Key '{rolc_id}' not found in bird_cube_structure_item_dictionary")
                print(f"ERROR: Available ANCRDT keys: {[k for k in sdd_context.bird_cube_structure_item_dictionary.keys() if 'ANCRDT' in k]}")
                continue

            # Create output class using modular function
            output_class = create_output_class(rolc_id, cube_structure_items)
            all_classes.append(output_class)

            # Create output table class using modular function
            output_table_class = create_output_table_class(rolc_id)
            all_classes.append(output_table_class)

        # Build complete module with all classes
        source_code = build_complete_module(imports, all_classes, filename)

        # Write to file
        with open(file_path, 'w', encoding='utf-8') as file:
            file.write(source_code)

        # Finalize
        CreatePythonTransformations._finalize_generated_file(file_path, False, logger)

    def create_slice_classes(sdd_context, logger):
        """Generate slice/logic classes for ANCRDT with lifecycle management (AST-based)"""
        from pybirdai.process_steps.ancrdt_transformation.ancrdt_transformation_func.ast_builders import create_import
        from collections import defaultdict

        cube_link_to_foreign_cube_map__ = {
            rolc_id: cube_links
            for rolc_id, cube_links in sdd_context.cube_link_to_foreign_cube_map.items()
            if "ANCRDT" in rolc_id
        }

        for rolc_id, cube_links in cube_link_to_foreign_cube_map__.items():
            file_path = sdd_context.output_directory + os.sep + 'generated_python_joins' + os.sep + rolc_id + '_logic.py'
            filename = rolc_id + '_logic.py'

            # Check if file has manual edits
            preserve = CreatePythonTransformations._prepare_generated_file(file_path, logger)
            if preserve:
                logger.info(f"Skipping generation of {filename} - manual edits detected")
                continue

            # Collect imports
            imports = [
                create_import('pybirdai.models.bird_data_model', None),  # import *
                create_import('pybirdai.process_steps.pybird.orchestration', ['Orchestration']),
                create_import('pybirdai.process_steps.pybird.csv_converter', ['CSVConverter']),
                create_import('datetime', ['datetime']),
                create_import('pybirdai.annotations.decorators', ['lineage', 'track_table_init']),
            ]

            # Collect all classes for this rolc_id
            all_classes = []

            # Get cube structure items
            if rolc_id in sdd_context.bird_cube_structure_item_dictionary:
                cube_structure_items = sdd_context.bird_cube_structure_item_dictionary[rolc_id]
                print(f"DEBUG: Found {len(cube_structure_items)} cube_structure_items for rolc_id={rolc_id}")
            else:
                print(f"ERROR: Key '{rolc_id}' not found in bird_cube_structure_item_dictionary")
                cube_structure_items = []

            # 1. Create UnionItem class
            union_item_class = create_union_item_class(rolc_id, cube_structure_items)
            all_classes.append(union_item_class)

            # 2. Create Base class
            base_class = create_base_class(rolc_id, cube_structure_items)
            all_classes.append(base_class)

            # 3. Create UnionTable class (needs join information)
            cube_link_to_join_for_report_id_map__ = {
                join_for_rolc_id: cube_links_list
                for join_for_rolc_id, cube_links_list in sdd_context.cube_link_to_join_for_report_id_map.items()
                if rolc_id in join_for_rolc_id
            }
            logger.info(str(cube_link_to_join_for_report_id_map__))

            union_table_class = create_union_table_class(rolc_id, cube_link_to_join_for_report_id_map__, logger)
            all_classes.append(union_table_class)

            # 4. Create Join classes (one per unique join_identifier)
            # Collect ALL cube_structure_item_links from ALL cube_links with the same join_id
            join_classes_created = []
            for join_for_rolc_id, cube_links_list in cube_link_to_join_for_report_id_map__.items():
                report_and_join = join_for_rolc_id.split(':')
                join_id = report_and_join[1]
                if report_and_join[0] == rolc_id and join_id not in join_classes_created:
                    # Collect cube structure item links for ALL cube_links with this join_id
                    all_cube_structure_item_links = []
                    for cube_link in cube_links_list:
                        try:
                            cube_structure_item_links = sdd_context.cube_structure_item_link_to_cube_link_map[cube_link.cube_link_id]
                            all_cube_structure_item_links.extend(cube_structure_item_links)
                        except KeyError:
                            logger.info(f"No cube structure item links for cube_link: {cube_link.cube_link_id}")

                    join_class = create_join_class(rolc_id, join_id, all_cube_structure_item_links, logger)
                    all_classes.append(join_class)
                    join_classes_created.append(join_id)

            # 5. Create Join Table classes
            join_table_classes_created = []
            for join_for_rolc_id, cube_links_list in cube_link_to_join_for_report_id_map__.items():
                report_and_join = join_for_rolc_id.split(':')
                join_id = report_and_join[1]
                if report_and_join[0] == rolc_id and join_id not in join_table_classes_created:
                    # Collect cube structure item links for this join
                    all_cube_structure_item_links = []
                    for cube_link in cube_links_list:
                        try:
                            cube_structure_item_links = sdd_context.cube_structure_item_link_to_cube_link_map[cube_link.cube_link_id]
                            all_cube_structure_item_links.extend(cube_structure_item_links)
                        except KeyError:
                            logger.info(f"No cube structure item links for cube_link: {cube_link.cube_link_id}")

                    join_table_class = create_join_table_class(rolc_id, join_id, all_cube_structure_item_links, logger)
                    all_classes.append(join_table_class)
                    join_table_classes_created.append(join_id)

            # 6. Create filtered/aggregated classes
            filtered_classes_created = []
            for join_for_rolc_id, cube_links_list in cube_link_to_join_for_report_id_map__.items():
                report_and_join = join_for_rolc_id.split(':')
                join_id = report_and_join[1]
                if report_and_join[0] == rolc_id and join_id not in filtered_classes_created:
                    # Prefetch all CUBE_STRUCTURE_ITEM_LINK objects for all cube_links at once
                    all_cube_structure_item_links = CUBE_STRUCTURE_ITEM_LINK.objects.filter(
                        cube_link_id__in=cube_links_list
                    ).select_related(
                        'cube_link_id__primary_cube_id',
                        'cube_link_id__foreign_cube_id',
                        'primary_cube_variable_code__variable_id',
                        'foreign_cube_variable_code__variable_id'
                    )

                    # Group links by cube_link in memory
                    links_by_cube = defaultdict(list)
                    for link in all_cube_structure_item_links:
                        links_by_cube[link.cube_link_id].append(link)

                    # Generate filter for calc_ function
                    filter_assignments = []
                    filter_bool_vars = []

                    for cube_link in cube_links_list:
                        cube_structure_item_link_ids = links_by_cube.get(cube_link, [])
                        for cube_structure_item_link in cube_structure_item_link_ids:
                            assignments, bool_var_names = TransformationBuildr.define_filter_from_structure_link(
                                cube_structure_item_link.cube_structure_item_link_id
                            )
                            filter_assignments.extend(assignments)
                            filter_bool_vars.extend(bool_var_names)

                    if not filter_bool_vars:
                        continue

                    # Generate mapping methods
                    assignment_dicts = dict()
                    for cube_link in cube_links_list:
                        cube_structure_item_link_ids = links_by_cube.get(cube_link, [])
                        for cube_structure_item_link in cube_structure_item_link_ids:
                            assignment_dicts.update(TransformationBuildr.reverse_apply_member_links(
                                cube_structure_item_link.cube_structure_item_link_id
                            ))

                    # Create filtered class pair (table and item)
                    filtered_table, filtered_item = create_filtered_class_pair(
                        rolc_id,
                        join_id,
                        cube_structure_items,
                        cube_links_list,
                        links_by_cube,
                        assignment_dicts,
                        TransformationBuildr,
                        CUBE_STRUCTURE_ITEM_LINK,
                        logger
                    )
                    all_classes.append(filtered_table)
                    all_classes.append(filtered_item)
                    filtered_classes_created.append(join_id)

            # Build complete module
            source_code = build_complete_module(imports, all_classes, filename)

            # Write to file
            with open(file_path, 'w', encoding='utf-8') as file:
                file.write(source_code)

            # Finalize for this rolc_id
            CreatePythonTransformations._finalize_generated_file(file_path, False, logger)


    def delete_generated_python_join_files(context):
        base_dir = settings.BASE_DIR
        python_dir = os.path.join(base_dir, 'results',  'generated_python_joins')
        for file in os.listdir(python_dir):
            os.remove(os.path.join(python_dir, file))
